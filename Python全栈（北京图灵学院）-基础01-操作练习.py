# 	·注释的写法
# 		·行注释：以井号开头，可以单独行，也可以在某行代码后边
# 		·块注释：好几行代码或者内容，以三个连续的单引号或者双引号开始和结束，中间任何内容机器都忽略
# 二、变量
# ·程序=数据结构+算法
# ·变量就是可以重复使用的一个量，或者叫一个代号
# ·变量命名的规则（有明规则及暗规则）
# 	·变量命名可以包含数字，大小写字母，下划线或者更多，但是我们不推荐除了前三种内容之外的符号
# 	·数字不可以打头
# 		-4man，5for 是不可以的
# 		-man4，for5 是不可以的
# 	·一般在Python中，以下划线开头的内容具有特殊意义，不建议使用
# 		-比如_age,_name,理论上可以，但是强烈不推荐，包括但不限于一个下划线开头和两个连续下划线开头的
# 	·大小写不一样，俗称大小写敏感
# 		-ForMan 跟 forMan不是一个变量名称
# 	·推荐
# 		-使用具有固定含义的英文单词或者缩写，srv=server skt=socket，一般以posix命名规则为主
# 		-推荐驼峰命名法
# 			-名称以单词自动连接，且每个单词首字母大写
# 				-大驼峰，每个单词第一个字母均大写
# 					-在Python中给类命名使用此法
# 					-MyFirstLove，IntOne
# 				-小驼峰，类似大驼峰，但第一个字母小写
# 					-在Python中一般给普通变量或者函数命名
# 					-myFirstLove，intOne
# 				posix写法
# 					-多个单词用下划线连接
# 					-单词全部小写
# 					-my_first_love，int_one
