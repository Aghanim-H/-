# 单双引号交错使用的例子
# 用引号表示下面这段字符串
# <a style="color:red">哈哈哈</a>
# 直接加双引号在外层打印会报错，为了不报错，只能暂时把字符串里面的双引号改为单引号
html = "<a style='color:red'>哈哈哈</a>"
print(html)
# 在外层加单引号再打印，注意单引号只能作用于单行
html = '<a style="color:red">哈哈哈</a>'
print(html)
